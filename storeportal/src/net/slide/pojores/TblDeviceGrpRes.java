package net.slide.pojores;

import java.util.Date;


public class TblDeviceGrpRes implements java.io.Serializable {

	private int id;
	private String groupName;
	private int deviceCount;
	private Date updatedDate;
	private String updatedBy;

	public TblDeviceGrpRes() {
		super();
	}
	public TblDeviceGrpRes(int id, String groupName, int deviceCount, Date updatedDate, String updatedBy) {
		super();
		this.id = id;
		this.groupName = groupName;
		this.deviceCount = deviceCount;
		this.updatedDate = updatedDate;
		this.updatedBy = updatedBy;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public int getDeviceCount() {
		return deviceCount;
	}
	public void setDeviceCount(int deviceCount) {
		this.deviceCount = deviceCount;
	}
	public Date getUpdatedDate() {
		return updatedDate;
	}
	public void setUpdatedDate(Date updatedDate) {
		this.updatedDate = updatedDate;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
}
